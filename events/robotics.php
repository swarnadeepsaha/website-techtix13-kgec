                               <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Problem Statement:</h2></p>
                                <p>Participants will have to make a manually controlled wireless or wired robot which will have to be capable of various terrains setups & will have to travel over the track loaded with marbles, sand, gravels, blocks, obstacles,pebbles and push the balls. It will be a stumper ball.  </p>
 
 <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Rules:</h2></p>
<p>1.     There can be maximum of 4 members in a participating group.</br>

2.     Every participating team must have their individual bot.</br>

3.     When the game will start, only one participant is allowed to enter the arena.</br>

4.     In case of any component failure of power supply or any other faults that appear on the bot during its actual run, for which participants are responsible, they may choose a “restart” which will cause loss in points.</br>

5.	Teams cannot modify their bots in the arena.</br>
6.	Any kind of damage caused to the arena by bot will lead to straight disqualification of the team.</br>
7.	LEGO kits or its spare parts are not allowed, but the participant can use ready-made gear boxes or bases.</br>


8.     There will be 4 rounds- Prelims, top 20, top 10 & final. There might be slight changes in arena from top 10 round.</br>
</p>
                                  <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Bot Specifications:</h2></p>
                                  <p>1.     Robot dimension should not be more than 25cm*25cm*25cm (5% tolerance). In case it is found exceeding the given dimension, your team shall be disqualified.</br>

2.     The motors of your robot should not exceed 48V DC motors.</br>

3.     220v 50Hz AC supply will be provided to the participants.</br>
</p>
<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Arena:</h2></p>
<img src="arena_img/TURBULENCE.jpg" style="height:500px; width:900px;" title="turbulance"/>
 <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Game Play:</h2></p>

<b>For Prelims & top 20:</b>
<p>
1.	Bot will start from the START GROUND.</br>
2.	PI-CURVE: There will be a 180° curve track, with 2 thin straps of tracks- one for the left wheels and one for the right wheels.</br>
3.	POLES: Bot has to cross among the 3 poles in a zigzag path.</br>
4.	FILL UP GAP WITH BLOCKS: By pushing the blocks in to the gap bot will able to go ahead.</br>
5.	ROTATING DISC: There will be a rotating disc. Rotating with 45 rpm speed. You have to complete a 360° rotation. After completing the rotation you will able to go next step.</p>
<b>For Top 10 & Final:</b>

<p>PUSHING THE BALL: There will be a ball & three holes. You have to push the ball in to the middle one otherwise there will be deduction of points.</br>
The bot has to go along all hurdles to complete the arena. All hurdles have its points and checkpoints. If any bot fails to complete any hurdle then the checkpoint will be missed and point corresponding to that checkpoint will not be counted. Team can restart the checkpoint. After completing the arena the bot will be judged on the basis of both points and time taken to complete the arena.</p>




