                                <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">EVENT SPECIFICATION:</h2></p>
                                <p>Your aim in this event is to make an autonomous line tracing robot that will detect boom barrier and stop until it is not removed. Each team will comprise of maximum 5 members .Winner will be decided on the basis of timing, accuracy and points.<br/>
</p>

                                <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Problem Statement & Specification:</h2></p>
                                <P>Make an Autonomous line following robot to trace black line of 4 cm width (background will be white). Robot should detect boom barrier of white colour and wait until boom barrier is not removed.<br/>
                                
                               1.Background of the track will be white and there will be black line of 4 cm width.<br/>
							   2.Track contains several curves (like circular, sharp 90 degrees etc.) and one or more boom barriers.<br/>
							   3.There will be crossing lines near boom barrier at a certain distance (10 cm approx.) from barriers, if a bot crosses these lines or touches boom barriers will lose valuable points.<br/>
							   4.Breadth of boom barrier (white colour) will be 6 cm .lower edge of barriers will be at a height of 6 cm from ground.<br/>
							   5.Initially the boom barrier is kept down, after detecting it bot will stop and wait until the barrier is removed automatically after few second.<br/>


</P>
 <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Game Play:</h2></p>
                                <P>
                                
                               1.In prelims the bot will only have to trace the Black strip of the track.<br/>
							   2.Boom barrier will be introduced in the Final Round.<br/>
							   3.Negative marking for restart and derailment of bot from the track.<br/>
							   4.Bonus marking will be given if the bot stops very near to the boom barrier (pre assigned method of measurement).<br/>
							   5.Only one person is allowed from each team to enter in the arena.<br/>
							   6.Your aim is to reach the end point at shortest time.<br/>
7.In final, best lap from the 5 consecutive lap will be considered.</br> 


</P><p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Marks Scheme:</h2></p>
                                <P>
                                
                               1."200" Negative marks for restart in case of the derailment of the bot.<br/>
							   2."500" points will be deducted if someone touches the in run without prior permission of the bot.<br/>
							   3."100" marks for reset from 1<sup>st</sup> checkpoint, "200" for 2<sup>nd</sup> checkpoint and "300" for last checkpoint. <br/>
							   4."200" bonus marks will be awarded if the bot will stop at the END point.</br> 
							   5. If the bot touches the Boom Barrier penalty of 'average distance' will be considered as <b>distance to stop</b> from the boom barrier and the bot must reset from last checkpoint.<br/>
5.If the bot make a serious <b>HIT</b> in the Boom Barrier the specific lap will be for the bot will be end there.</br>
							   6.The team will be disqualified for damage of the arena and the boom barrier.<br/>


</P>
