<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">
PROBLEM STATEMENT</h2></p>
<p>Design a manually controlled robot to battle with the opponent ,to demobilise it , to push it out of the battle field & also to collect checkpoints placed at different positions of the battle arena with specified time.</p>
<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">
SPECIFICATIONS 
</h2></p>
<p>
1.&nbsp; Robot dimensions not to exceed 25x25x25 cm.<br/>
2.&nbsp;Has to be a manual robot <br/>
3.&nbsp;Can have any sort of attack or defense mechanism. But you are not allowed to have any cutting and melting mechanism built inside your bot. If the jury members find you violating this, your bot will be disqualified. 
<br/>
4.&nbsp;You are allowed to use a power supply of atmost 20V. We will provide you with a 220V AC supply which you may/may not use. 
<br/>
5.&nbsp;Team can consist of FIVE(5) participants, but at any point of time robot will be handled using only two members, one driver and one helper. Driver has to remain constant throughout the round. 
<br/>
6.&nbsp;If you are found causing any kind of damage to the arena or surroundings while competing, you may be disqualified. 

<br/>
</p>
<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">
GENERAL RULES 
</h2></p>

<p>
1.&nbsp;There are points allotted for pushing the opponent bot out of the arena .The bot is declared a winner if it pushes the opponent bot 3 times out of the arena . 
 <br/>
2.&nbsp;Bonus points will be awarded for demobilising the opponent robot (partially/entirely). In case of dispute/ doubt the judgment of the coordinator shall be final. 
<br/>
3.&nbsp;The duration of the match will be of 9 mins.The arena will consist of three check points each of which will be activated for a duration of 1 min in between the match.The bot will be awarded points for collecting each checkpoint on time.
 
<br/>
4.&nbsp;The activation of the checkpoint will be denoted by switching ON a bulb for 1 min.The bot is required to switch OFF the bulb in order to get the points for the checkpoint.For switching OFF the bulb,a switch will be provided which will be placed vertically & at a height of 7.5 cm from the ground.

<br/>
5.&nbsp;The event has 3 winners. 
 
<br/>
6.&nbsp;The power of judgment rests with the event coordinator. His decision shall prevail. 


<br/>
7.&nbsp;During the match,each team will be allowed to take  2 min restarts in case of any kind of problems like lose connection,problems related to power supply or if the bot gets stuck in the arena etc.However ,points will be deducted for taking a restart.
<br/>
8.&nbsp;If none of the bots are pushed out of the arena 3 times,then the bot with higher points will  be declared the winner.  

<br/>
</p>
