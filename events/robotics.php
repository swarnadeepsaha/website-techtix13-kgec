<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Problem Statement:</h2></p>
<p>Two teams will play soccer with two manually controlled robots.</p>
                              <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">General Rules:</h2></p>
<p> 1.	Each team can have maximum of 5 members.</br>

    2.	Teams can have maximum two manually controlled bots controlled by the team members only.</br>
    3.	The participants will be provided with 220volts 50Hz standard AC supply. Participants will have to themselves arrange for any other power    supply, SMPS ,adapters etc. required for their robot, these cords/cables should remain slack throughout the event.</br>

    4.	 LEGO kits or its spare parts are not allowed.</br>

    5.	Any kind of damage caused to the arena by bots will lead to disqualification.</br>

</p>
<p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">ROBOT SPECIFICATIONS:</h2></p>
<p>
1.Robots can have maximum size at any instant of the play around 25cm*25cm*25 cm with
10% tolerance. Teams are requested to maintain the dimensions positively.<br/>
2. No external weight must be attached to the bots.<br/>
3.Any kind of metal part will not be allowed except the L-clamp, Motor
parts and metal required to implement some kind of mechanism. We will love
to see a good game, not brawl. Chassis should be made of non-metallic
substances (e.g.- wood).</br>

</p>

   <p><h2 style="font-family:Georgia, 'Times New Roman', Times, serif; font-size:20px;">Gameplay & Arena:</h2></p>
<p>
1. For each goal scored 200 points will be awarded. </br>

    2.	One time outs/ team will be given for 5 minutes, if required.  However, in case of further glitches the decision of the on spot       coordinators will be considered as final, the maximum time allowed for the event will be 10 minutes, the decisions of the judges are final and   binding, and the right spirit of participation is expected from the participants.</br>

     3.	The match would comprise of 2 halves, 3 minutes each.</br>

     4.	In case of a draw match, game will be continued for 2 more minutes. </br>

     5.	Semifinals and finals would comprise of 2 halves, 5 minutes each.</br>

   6. Participants cannot step inside the arena at any point of the match.</br>

   7. The game would be played by table tennis balls.</br>

   8. Trapping of ball for more than 5 seconds or obstructing the opponent deliberately is
prohibited. Trapping of ball for more than 5 seconds will lead to deduction of 50 points.</br>

    9. Shooting the ball outside the arena will lead to deduction of 50 points.</br>

  10. If the bots are unable to move because of being jammed or being tangled by wire for
more than 10 seconds then the play will be resumed from the middle point.</br>

   11. The dimension of the arena will be 400cm*500cm. The goal post length and height will
be 80cm and 40 cm respectively.</br>
</p>
