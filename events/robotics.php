To suffice that need, KGEC calls everyone to arms with your bot by your side. Come to witness the thwak, thunk, metal on metal carnage. Cut, hammer, break, crash, push, destroy, trash, dismantle, smash, axe, nuke, suppress, tear apart, annihilate…. but that’s just the beginning. Time has proved it is just not an individual’s might that can alone win wars as they say “Battles are won by both slaughter and maneuver”.
