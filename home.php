A radio-controlled (model) aircraft (often called RC aircraft or RC
plane) is a small flying machine that is controlled remotely by an
operator on the ground using a hand-held radio transmitter. The
transmitter communicates with a receiver within the craft that sends
signals to servomechanisms(servos) which move the control surfaces
based on the position of joysticks on the transmitter. The control
surfaces, in turn, affect the orientation of the plane.

Flying RC aircraft as a hobby has been growing worldwide with the
advent of more efficient motors (both electric and miniature internal
combustion orjet engines), lighter and more powerful batteries and
less expensive radio systems. A wide variety of models and styles is
available.

Scientific, government and military organizations are also utilizing
